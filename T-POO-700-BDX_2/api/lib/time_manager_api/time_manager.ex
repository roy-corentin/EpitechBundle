defmodule TimeManagerApi.TimeManager do
  @moduledoc """
  The TimeManager context.
  """

  import Ecto.Query, warn: false
  alias TimeManagerApi.Repo

  alias TimeManagerApi.TimeManager.WorkingTime

  @doc """
  Returns the list of workingtimes.

  ## Examples

      iex> list_workingtimes()
      [%WorkingTime{}, ...]

  """
  def list_workingtimes do
    Repo.all(WorkingTime)
  end

  @doc """
  Returns the list of workingtimes of a user.

  ## Examples

      iex> list_user_workingtimes(2)
      [%WorkingTime{}, ...]

  """
  def list_user_workingtimes(user_id) do
    Repo.all(
      from(wt in WorkingTime,
        where: wt.user_id == ^String.to_integer(user_id),
        select: wt
      )
    )
  end

  @doc """
  Returns the list of workingtimes of a user >= to start and <= to end.

  ## Examples

      iex> find_user_workingtimes(2, a_start_datetime, an_end_datetime)
      [%WorkingTime{}, ...]

  """
  def find_user_workingtimes(user_id, %{:start => start, :end_date => end_date}) do
    Repo.all(
      from(wt in WorkingTime,
        where:
          wt.user_id == ^String.to_integer(user_id) and wt.start >= ^start and
            wt.end <= ^end_date,
        select: wt
      )
    )
  end

  def find_user_workingtimes(user_id, %{:start => start}) do
    Repo.all(
      from(wt in WorkingTime,
        where: wt.user_id == ^String.to_integer(user_id) and wt.start >= ^start,
        select: wt
      )
    )
  end

  def find_user_workingtimes(user_id, %{:end_date => end_date}) do
    Repo.all(
      from(wt in WorkingTime,
        where: wt.user_id == ^String.to_integer(user_id) and wt.end <= ^end_date,
        select: wt
      )
    )
  end

  @doc """
  Gets a single working_time.

  Raises `Ecto.NoResultsError` if the Working time does not exist.

  ## Examples

      iex> get_working_time!(123)
      %WorkingTime{}

      iex> get_working_time!(456)
      ** (Ecto.NoResultsError)

  """
  def get_working_time!(id), do: Repo.get!(WorkingTime, id)

  @doc """
  Creates a working_time.

  ## Examples

      iex> create_working_time(%{field: value})
      {:ok, %WorkingTime{}}

      iex> create_working_time(%{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def create_working_time(attrs \\ %{}) do
    %WorkingTime{}
    |> WorkingTime.changeset(attrs)
    |> Repo.insert()
  end

  @doc """
  Updates a working_time.

  ## Examples

      iex> update_working_time(working_time, %{field: new_value})
      {:ok, %WorkingTime{}}

      iex> update_working_time(working_time, %{field: bad_value})
      {:error, %Ecto.Changeset{}}

  """
  def update_working_time(%WorkingTime{} = working_time, attrs) do
    working_time
    |> WorkingTime.changeset(attrs)
    |> Repo.update()
  end

  @doc """
  Deletes a working_time.

  ## Examples

      iex> delete_working_time(working_time)
      {:ok, %WorkingTime{}}

      iex> delete_working_time(working_time)
      {:error, %Ecto.Changeset{}}

  """
  def delete_working_time(%WorkingTime{} = working_time) do
    Repo.delete(working_time)
  end

  @doc """
  Returns an `%Ecto.Changeset{}` for tracking working_time changes.

  ## Examples

      iex> change_working_time(working_time)
      %Ecto.Changeset{data: %WorkingTime{}}

  """
  def change_working_time(%WorkingTime{} = working_time, attrs \\ %{}) do
    WorkingTime.changeset(working_time, attrs)
  end

